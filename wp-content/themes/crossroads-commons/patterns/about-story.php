<section class="story">
  <p>In the winter of 2023, key leaders in the community gathered in the empty Crossroads Mall to ask a simple question: Could this space be dedicated to the flourishing of children in Oklahoma City?</p>

  <p>We shared a collective concern for the challenges facing children in Oklahoma and felt a deep responsibility to help address them. The mall was for sale, and already 4,000 students attended school on the property each day. What if we added more schools&mdash;and filled the remaining 1.2 million square feet with the services, resources, and stores that families need to thrive? What if we made it beautiful?</p>

  <p>The people in that meeting believed the idea was worth exploring. A few months later, a nonprofit called Crossroads Renewal Project was born. Generous donors stepped forward to make the vision possible, providing the funds for CRP to acquire the property. We began working with architects and engineers and welcoming our first tenants.</p>

  <p class="highlight-name">Today, the property is called Crossroads Commons.</p>

  <p>Crossroads Commons is a place for everyone. Our goal is not only to bless the students who attend school on site, but to be a signpost of hope for all children in Oklahoma&mdash;a place that communicates clearly that they are cared for, loved, and seen.</p>

  <p class="welcome">All children and families are welcome here.</p>

  <p>We are currently seeking additional tenants and donors to partner with us in the restoration, renovation, and beautification of the property. Over time, lease income will support the long-term sustainability of this work.</p>

  <p>We plan to open the first several thousand square feet of healthcare space in the winter of 2026. Follow our blog or newspaper for updates as the story continues.</p>
